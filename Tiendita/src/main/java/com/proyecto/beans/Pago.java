package com.proyecto.beans;

public class Pago {
	private int clave;
	private float montoComision;
	private float totalPag;
	private int claveEmpleado;
	public int getClave() {
		return clave;
	}
	public void setClave(int clave) {
		this.clave = clave;
	}
	public float getMontoComision() {
		return montoComision;
	}
	public void setMontoComision(float montoComision) {
		this.montoComision = montoComision;
	}
	public float getTotalPag() {
		return totalPag;
	}
	public void setTotalPag(float totalPag) {
		this.totalPag = totalPag;
	}
	public int getClaveEmpleado() {
		return claveEmpleado;
	}
	public void setClaveEmpleado(int claveEmpleado) {
		this.claveEmpleado = claveEmpleado;
	}
	
	
}
