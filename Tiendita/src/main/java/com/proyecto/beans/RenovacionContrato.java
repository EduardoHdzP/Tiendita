package com.proyecto.beans;

public class RenovacionContrato {
	private int numero;
	private int claveEmpleado;
	private String nPuesto;
	private String nfInicio;
	private String nfFin;
	private String nSueldo;
	private String nStatus;
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	public int getClaveEmpleado() {
		return claveEmpleado;
	}
	public void setClaveEmpleado(int claveEmpleado) {
		this.claveEmpleado = claveEmpleado;
	}
	public String getnPuesto() {
		return nPuesto;
	}
	public void setnPuesto(String nPuesto) {
		this.nPuesto = nPuesto;
	}
	public String getNfInicio() {
		return nfInicio;
	}
	public void setNfInicio(String nfInicio) {
		this.nfInicio = nfInicio;
	}
	public String getNfFin() {
		return nfFin;
	}
	public void setNfFin(String nfFin) {
		this.nfFin = nfFin;
	}
	public String getnSueldo() {
		return nSueldo;
	}
	public void setnSueldo(String nSueldo) {
		this.nSueldo = nSueldo;
	}
	public String getnStatus() {
		return nStatus;
	}
	public void setnStatus(String nStatus) {
		this.nStatus = nStatus;
	}
	
	

}
